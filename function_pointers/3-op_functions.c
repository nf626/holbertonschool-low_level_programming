#include "3-calc.h"

/**
 * op_add - the sum of a and b.
 * @a: value a.
 * @b: value b.
 *
 * Return: sum of a + b.
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - the difference of a and b. 
 * @a: value a.
 * @b: value b.
 *
 * Return: difference of a - b.
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - the product of a and b.
 * @a: value a.
 * @b: value b.
 *
 * Return: product of a x b.
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - the result of the division of a by b.
 * @a: value a.
 * @b: value b.
 *
 * Return: division of a / b.
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - the remainder of the division of a by b.
 * @a: value a.
 * @b: value b.
 *
 * Return: remainder of a % b.
 */
int op_mod(int a, int b)
{
return (a % b);
}
